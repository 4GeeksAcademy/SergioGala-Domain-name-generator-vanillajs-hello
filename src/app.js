function getRandomElement(array) {
  return array[Math.floor(Math.random() * array.length)];
}

function generateRandomDomain() {
  const name1 = getRandomElement(casa);
  const adjective = getRandomElement(grande);
  const name2 = getRandomElement(libro);
  const adverb = getRandomElement(rapidamente);
  const verb = getRandomElement(correr);
  const extension = getRandomElement(extensiones);

  return `${adjective}${name1}${name2}${adverb}${verb}${extension}`;
}
let intervalId;

function startGeneratingDomains() {
  intervalId = setInterval(() => {
    document.getElementById("generator").innerText = generateRandomDomain();
  }, 500);
}

function stopGeneratingDomains() {
  clearInterval(intervalId);
}

document.getElementById("toggleButton").addEventListener("click", function() {
  if (intervalId) {
    stopGeneratingDomains();
    this.textContent = "Start";
  } else {
    startGeneratingDomains();
    this.textContent = "Stop";
  }
});
startGeneratingDomains();
